const textNodes = document.querySelectorAll("[data-i18n]");
const langButtons = document.querySelectorAll(".lang-btn");
const menuToggle = document.querySelector(".menu-toggle");
const siteNav = document.querySelector(".site-nav");
const slides = document.querySelectorAll(".slide");
const sliderDots = document.querySelectorAll(".slider-dot");
const galleryItems = document.querySelectorAll(".gallery-item");
const lightbox = document.querySelector(".lightbox");
const lightboxImage = document.querySelector(".lightbox-image");
const lightboxCaption = document.querySelector(".lightbox-caption");
const lightboxClose = document.querySelector(".lightbox-close");
const scrollTopButton = document.querySelector(".scroll-top");
const revealItems = document.querySelectorAll(".reveal");

let currentLang = "en";
let currentSlide = 0;
let slideTimer = null;

function updateMeta(lang) {
  const meta = translations[lang].meta;
  document.documentElement.lang = lang;
  document.title = meta.title;
  document.querySelector('meta[name="description"]').setAttribute("content", meta.description);
  document.querySelector('meta[property="og:title"]').setAttribute("content", meta.ogTitle);
  document.querySelector('meta[property="og:description"]').setAttribute("content", meta.ogDescription);
  document.querySelector('meta[name="twitter:title"]').setAttribute("content", meta.twitterTitle);
  document.querySelector('meta[name="twitter:description"]').setAttribute("content", meta.twitterDescription);
}

function applyTranslations(lang) {
  currentLang = lang;
  const dictionary = translations[lang];

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });

  updateMeta(lang);
  const nextUrl = new URL(window.location.href);
  if (lang === "en") {
    nextUrl.searchParams.delete("lang");
  } else {
    nextUrl.searchParams.set("lang", lang);
  }
  window.history.replaceState({}, "", nextUrl);
}

function showSlide(index) {
  currentSlide = (index + slides.length) % slides.length;
  slides.forEach((slide, slideIndex) => {
    slide.classList.toggle("is-active", slideIndex === currentSlide);
  });
  sliderDots.forEach((dot, dotIndex) => {
    dot.classList.toggle("is-active", dotIndex === currentSlide);
  });
}

function startSlider() {
  clearInterval(slideTimer);
  slideTimer = window.setInterval(() => {
    showSlide(currentSlide + 1);
  }, 4200);
}

sliderDots.forEach((dot, index) => {
  dot.addEventListener("click", () => {
    showSlide(index);
    startSlider();
  });
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => applyTranslations(button.dataset.lang));
});

menuToggle?.addEventListener("click", () => {
  const isOpen = siteNav.classList.toggle("is-open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

document.querySelectorAll(".site-nav a").forEach((link) => {
  link.addEventListener("click", () => {
    siteNav.classList.remove("is-open");
    menuToggle?.setAttribute("aria-expanded", "false");
  });
});

galleryItems.forEach((item) => {
  item.addEventListener("click", () => {
    lightboxImage.src = item.dataset.full;
    lightboxImage.alt = item.querySelector("img")?.alt || "";
    lightboxCaption.textContent = currentLang === "es" ? item.dataset.captionEs : item.dataset.captionEn;
    lightbox.showModal();
  });
});

function closeLightbox() {
  lightbox.close();
  lightboxImage.src = "";
}

lightboxClose.addEventListener("click", closeLightbox);
lightbox.addEventListener("click", (event) => {
  const bounds = lightbox.getBoundingClientRect();
  const clickedOutside =
    event.clientX < bounds.left ||
    event.clientX > bounds.right ||
    event.clientY < bounds.top ||
    event.clientY > bounds.bottom;

  if (clickedOutside) {
    closeLightbox();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.open) {
    closeLightbox();
  }
});

scrollTopButton.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

window.addEventListener("scroll", () => {
  scrollTopButton.classList.toggle("is-visible", window.scrollY > 500);
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.18 });

revealItems.forEach((item) => revealObserver.observe(item));

const params = new URLSearchParams(window.location.search);
const initialLang = params.get("lang") === "es" ? "es" : "en";
applyTranslations(initialLang);
showSlide(0);
startSlider();
